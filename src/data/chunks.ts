// 오늘의 학습 청크 가져오기 (언어 지원)
export function getTodayChunks(trackId: TrackId, count: number = 3, language: LearningLanguage = 'en'): ChunkData[] {
  const languageChunks = CHUNKS_BY_LANGUAGE[language] || CHUNKS_BY_TRACK;
  const chunks = languageChunks[trackId] || [];

  if (chunks.length === 0) {
    // 해당 트랙에 데이터가 없으면 기본 생활 회화 반환
    const defaultChunks = languageChunks['daily-life'] || DAILY_LIFE_CHUNKS;
    return defaultChunks.slice(0, count);
  }
  // 간단히 처음 count개 반환 (실제로는 학습 진도에 따라 결정)
  return chunks.slice(0, count);
}

// 복습 퀴즈 데이터 생성
export function generateQuizFromChunks(chunks: ChunkData[]) {
  return chunks.map((chunk, idx) => {
    const types = ['meaning', 'expression'] as const;
    const type = types[idx % types.length];

    if (type === 'meaning') {
      return {
        id: `quiz-${chunk.id}`,
        type: 'meaning' as const,
        question: '다음 표현의 의미는?',
        expression: chunk.expression,
        correctAnswer: chunk.meaning,
        options: shuffleArray([
          chunk.meaning,
          getRandomWrongAnswer(chunk.meaning),
          getRandomWrongAnswer(chunk.meaning),
          getRandomWrongAnswer(chunk.meaning),
        ]),
      };
    } else {
      return {
        id: `quiz-${chunk.id}`,
        type: 'expression' as const,
        question: '다음 의미에 맞는 표현은?',
        meaning: chunk.meaning,
        correctAnswer: chunk.expression,
        options: shuffleArray([
          chunk.expression,
          getRandomWrongExpression(),
          getRandomWrongExpression(),
          getRandomWrongExpression(),
        ]),
      };
    }
  });
}

// 헬퍼 함수들
function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

const WRONG_ANSWERS = [
  "그건 좀 어려울 것 같아요.",
  "다음에 다시 이야기해요.",
  "잘 모르겠어요.",
  "도움이 필요하시면 말씀하세요.",
  "생각해 볼게요.",
  "그것에 대해 더 알아볼게요.",
];

const WRONG_EXPRESSIONS = [
  "I'm not sure about that.",
  "Let me think about it.",
  "That sounds interesting.",
  "I'll get back to you.",
  "Could you repeat that?",
  "I need more time.",
];

function getRandomWrongAnswer(correct: string): string {
  const filtered = WRONG_ANSWERS.filter(a => a !== correct);
  return filtered[Math.floor(Math.random() * filtered.length)];
}

function getRandomWrongExpression(): string {
  return WRONG_EXPRESSIONS[Math.floor(Math.random() * WRONG_EXPRESSIONS.length)];
}
